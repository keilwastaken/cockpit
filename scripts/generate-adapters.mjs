#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  actionMappings,
  bootstrapMarker,
  opencodeDoctorPrompt,
  opencodeSetupPrompt,
} from "./adapter-definition.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const check = process.argv.includes("--check");

function skillBody(markdown) {
  const match = markdown.match(/^---\r?\n[\s\S]*?\r?\n---\r?\n([\s\S]*)$/);
  return (match ? match[1] : markdown).trim();
}

const usingCockpit = skillBody(fs.readFileSync(path.join(root, "skills/using-cockpit/SKILL.md"), "utf8"));

function js(value) {
  return JSON.stringify(value);
}

function renderOpenCode() {
  return `// Generated by scripts/generate-adapters.mjs. Do not edit directly.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const pluginDirectory = path.dirname(fileURLToPath(import.meta.url));
const skillsDirectory = path.resolve(pluginDirectory, "../../skills");
const bootstrapMarker = ${js(bootstrapMarker)};
const setupPrompt = ${js(opencodeSetupPrompt)};
const doctorPrompt = ${js(opencodeDoctorPrompt)};
const bootstrapPattern = /^COCKPIT_BOOTSTRAP_V\\d+\\n\\nYou have Cockpit skills available\\. The using-cockpit skill is already loaded below; do not load it again for this conversation\\./;
let cachedBootstrap;

function skillBody(markdown) {
  const match = markdown.match(/^---\\r?\\n[\\s\\S]*?\\r?\\n---\\r?\\n([\\s\\S]*)$/);
  return match ? match[1].trim() : markdown.trim();
}

function bootstrap() {
  if (cachedBootstrap !== undefined) return cachedBootstrap;
  const bootstrapPath = path.join(skillsDirectory, "using-cockpit", "SKILL.md");
  if (!fs.existsSync(bootstrapPath)) return (cachedBootstrap = null);
  const content = skillBody(fs.readFileSync(bootstrapPath, "utf8"));
  cachedBootstrap = ${js(`${bootstrapMarker}\n\nYou have Cockpit skills available. The using-cockpit skill is already loaded below; do not load it again for this conversation.\n\n`)} + content + ${js(`\n\nOpenCode action mapping:\n${actionMappings.opencode.map((line) => `- ${line}`).join("\n")}\n\nCanonical Cockpit skills describe actions rather than requiring these specific tools.`)};
  return cachedBootstrap;
}

export const CockpitPlugin = async () => ({
  config: async (config) => {
    config.skills ??= {};
    config.skills.paths ??= [];
    if (!config.skills.paths.includes(skillsDirectory)) config.skills.paths.push(skillsDirectory);
    config.command ??= {};
    config.command["cockpit-setup"] ??= { description: "Choose reasoning and hands models for Cockpit roles", template: setupPrompt };
    config.command["cockpit-doctor"] ??= { description: "Diagnose Cockpit skills and model assignments", template: doctorPrompt };
  },
  "experimental.chat.messages.transform": async (_input, output) => {
    const content = bootstrap();
    if (!content) return;
    const firstUserMessage = output.messages.find((message) => message.info.role === "user");
    if (!firstUserMessage) return;
    if (firstUserMessage.parts.some((part) => part.type === "text" && bootstrapPattern.test(part.text))) return;
    const referencePart = firstUserMessage.parts[0] ?? {};
    firstUserMessage.parts.unshift({ ...referencePart, type: "text", text: content });
  },
});
`;
}

const outputs = new Map([
  [".opencode/plugins/cockpit.js", renderOpenCode()],
]);

let stale = false;

for (const [relative, content] of outputs) {
  const target = path.join(root, relative);
  const current = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : null;
  if (current === content) continue;
  if (check) {
    console.error(`Stale generated artifact: ${relative}`);
    stale = true;
    continue;
  }
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content);
  console.log(`Generated ${relative}`);
}

if (stale) process.exitCode = 1;
