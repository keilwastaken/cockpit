#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  opencodeContractPrompt,
  opencodeDoctorPrompt,
  opencodeRunPrompt,
  opencodeSetupPrompt,
  opencodeWorkerDescription,
} from "./adapter-definition.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const check = process.argv.includes("--check");

function skillBody(markdown) {
  const match = markdown.match(/^---\r?\n[\s\S]*?\r?\n---\r?\n([\s\S]*)$/);
  return (match ? match[1] : markdown).trim();
}

const executeSkill = skillBody(fs.readFileSync(path.join(root, "skills/cockpit-execute/SKILL.md"), "utf8"));

function js(value) {
  return JSON.stringify(value);
}

function renderOpenCode() {
  return `// Generated by scripts/generate-adapters.mjs. Do not edit directly.
import path from "node:path";
import { fileURLToPath } from "node:url";

const pluginDirectory = path.dirname(fileURLToPath(import.meta.url));
const skillsDirectory = path.resolve(pluginDirectory, "../../skills");
const setupPrompt = ${js(opencodeSetupPrompt)};
const doctorPrompt = ${js(opencodeDoctorPrompt)};
const workerPrompt = ${js(`You are Cockpit's bounded execution worker. Follow this contract exactly.\n\n${executeSkill}`)};
const workerDescription = ${js(opencodeWorkerDescription)};
const workerPermission = { task: "deny", question: "deny", webfetch: "deny", skill: "deny" };

export const CockpitPlugin = async () => ({
  config: async (config) => {
    config.skills ??= {};
    config.skills.paths ??= [];
    if (!config.skills.paths.includes(skillsDirectory)) config.skills.paths.push(skillsDirectory);
    config.agent ??= {};
    const existingWorker = config.agent["cockpit-worker"] ?? {};
    const workerModel = existingWorker.model ?? config.small_model;
    const existingPermission = typeof existingWorker.permission === "string"
      ? { "*": existingWorker.permission }
      : (existingWorker.permission ?? {});
    config.agent["cockpit-worker"] = {
      ...existingWorker,
      description: existingWorker.description ?? workerDescription,
      model: workerModel,
      disable: workerModel ? existingWorker.disable : true,
      mode: "subagent",
      prompt: workerPrompt,
      steps: 20,
      permission: { ...existingPermission, ...(workerModel ? {} : { edit: "deny", bash: "deny" }), ...workerPermission },
    };
    config.command ??= {};
    config.command["cockpit-setup"] ??= { description: "Choose strong and hands models for Cockpit", template: setupPrompt };
    config.command["cockpit-doctor"] ??= { description: "Diagnose Cockpit skills and model assignments", template: doctorPrompt };
    config.command["cockpit-contract"] ??= { description: "Create a bounded execution contract without editing", template: ${js(opencodeContractPrompt)}, agent: "build", subtask: false };
    config.command["cockpit-run"] ??= { description: "Orchestrate execution of an approved contract on cockpit-worker from build", template: ${js(opencodeRunPrompt)}, agent: "build", subtask: false };
  },
});
`;
}

const outputs = new Map([
  [".opencode/plugins/cockpit.js", renderOpenCode()],
]);

let stale = false;

for (const [relative, content] of outputs) {
  const target = path.join(root, relative);
  const current = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : null;
  if (current === content) continue;
  if (check) {
    console.error(`Stale generated artifact: ${relative}`);
    stale = true;
    continue;
  }
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content);
  console.log(`Generated ${relative}`);
}

if (stale) process.exitCode = 1;
