#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  actionMappings,
  bootstrapMarker,
  opencodeDoctorPrompt,
  opencodeSetupPrompt,
  roles,
  skills,
} from "./adapter-definition.mjs";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const check = process.argv.includes("--check");

function skillBody(markdown) {
  const match = markdown.match(/^---\r?\n[\s\S]*?\r?\n---\r?\n([\s\S]*)$/);
  return (match ? match[1] : markdown).trim();
}

const usingCockpit = skillBody(fs.readFileSync(path.join(root, "skills/using-cockpit/SKILL.md"), "utf8"));

function bootstrap(harness) {
  return `${bootstrapMarker}

You have Cockpit skills available. The using-cockpit skill is already loaded below; do not load it again for this conversation.

${usingCockpit}

${harness} action mapping:
${actionMappings[harness.toLowerCase()].map((line) => `- ${line}`).join("\n")}

Canonical Cockpit skills describe actions rather than requiring these specific tools.`;
}

function js(value) {
  return JSON.stringify(value);
}

function renderOpenCode() {
  return `// Generated by scripts/generate-adapters.mjs. Do not edit directly.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const pluginDirectory = path.dirname(fileURLToPath(import.meta.url));
const skillsDirectory = path.resolve(pluginDirectory, "../../skills");
const bootstrapMarker = ${js(bootstrapMarker)};
const setupPrompt = ${js(opencodeSetupPrompt)};
const doctorPrompt = ${js(opencodeDoctorPrompt)};
let cachedBootstrap;

function skillBody(markdown) {
  const match = markdown.match(/^---\\r?\\n[\\s\\S]*?\\r?\\n---\\r?\\n([\\s\\S]*)$/);
  return match ? match[1].trim() : markdown.trim();
}

function bootstrap() {
  if (cachedBootstrap !== undefined) return cachedBootstrap;
  const bootstrapPath = path.join(skillsDirectory, "using-cockpit", "SKILL.md");
  if (!fs.existsSync(bootstrapPath)) return (cachedBootstrap = null);
  const content = skillBody(fs.readFileSync(bootstrapPath, "utf8"));
  cachedBootstrap = ${js(`${bootstrapMarker}\n\nYou have Cockpit skills available. The using-cockpit skill is already loaded below; do not load it again for this conversation.\n\n`)} + content + ${js(`\n\nOpenCode action mapping:\n${actionMappings.opencode.map((line) => `- ${line}`).join("\n")}\n\nCanonical Cockpit skills describe actions rather than requiring these specific tools.`)};
  return cachedBootstrap;
}

export const CockpitPlugin = async () => ({
  config: async (config) => {
    config.skills ??= {};
    config.skills.paths ??= [];
    if (!config.skills.paths.includes(skillsDirectory)) config.skills.paths.push(skillsDirectory);
    config.command ??= {};
    config.command["cockpit-setup"] ??= { description: "Choose reasoning and hands models for Cockpit roles", template: setupPrompt };
    config.command["cockpit-doctor"] ??= { description: "Diagnose Cockpit skills and model assignments", template: doctorPrompt };
  },
  "experimental.chat.messages.transform": async (_input, output) => {
    const content = bootstrap();
    if (!content) return;
    const firstUserMessage = output.messages.find((message) => message.info.role === "user");
    if (!firstUserMessage) return;
    if (firstUserMessage.parts.some((part) => part.type === "text" && part.text.includes(bootstrapMarker))) return;
    const referencePart = firstUserMessage.parts[0] ?? {};
    firstUserMessage.parts.unshift({ ...referencePart, type: "text", text: content });
  },
});
`;
}

function renderPi() {
  const content = bootstrap("Pi");
  return `// Generated by scripts/generate-adapters.mjs. Do not edit directly.
const bootstrapMarker = ${js(bootstrapMarker)};
const bootstrap = ${js(content)};
const canonicalSkills = ${js(skills)};

export default function cockpit(pi) {
  pi.on("before_agent_start", async (event) => {
    if (event.systemPrompt.includes(bootstrapMarker)) return;
    return { systemPrompt: event.systemPrompt + "\\n\\n" + bootstrap };
  });

  pi.registerCommand("cockpit-setup", {
    description: "Choose Pi's active model for sequential Cockpit workflows",
    handler: async (_args, ctx) => {
      if (!ctx.hasUI) return ctx.ui.notify("Cockpit setup requires interactive mode.", "warning");
      const models = await ctx.modelRegistry.getAvailable();
      if (models.length === 0) return ctx.ui.notify("No authenticated models are available.", "error");
      const providers = [...new Set(models.map((model) => model.provider))].sort();
      const provider = await ctx.ui.select("Cockpit model provider", providers);
      if (!provider) return;
      const providerModels = models.filter((model) => model.provider === provider);
      const labels = providerModels.map((model) => model.name ? model.id + " - " + model.name : model.id);
      const selectedLabel = await ctx.ui.select("Cockpit active model", labels);
      if (!selectedLabel) return;
      const selected = providerModels[labels.indexOf(selectedLabel)];
      const confirmed = await ctx.ui.confirm("Apply Cockpit setup?", "Use " + selected.provider + "/" + selected.id + " for this Pi session. Cockpit workflows remain sequential.");
      if (!confirmed) return;
      const changed = await pi.setModel(selected);
      ctx.ui.notify(changed ? "Cockpit model selected. Workflows run sequentially." : "Model unavailable: no API key found.", changed ? "info" : "error");
    },
  });

  pi.registerCommand("cockpit-doctor", {
    description: "Diagnose Cockpit's Pi integration without changing configuration",
    handler: async (_args, ctx) => {
      const available = await ctx.modelRegistry.getAvailable();
      const registered = pi.getCommands();
      const commands = registered.map((command) => command.name);
      const discoveredSkills = new Set(registered.filter((command) => command.source === "skill").map((command) => command.name));
      const missingSkills = canonicalSkills.filter((skill) => !discoveredSkills.has(skill));
      const checks = [
        "PASS extension: cockpit commands loaded",
        commands.includes("cockpit-setup") && commands.includes("cockpit-doctor") ? "PASS commands: setup and doctor registered" : "FAIL commands: setup or doctor missing",
        missingSkills.length === 0 ? "PASS skills: all canonical skills discovered" : "FAIL skills: missing " + missingSkills.join(", "),
        ctx.model ? "PASS model: " + ctx.model.provider + "/" + ctx.model.id : "WARN model: no active model",
        available.length > 0 ? "PASS models: " + available.length + " authenticated model(s) available" : "FAIL models: none authenticated",
        "PASS execution: sequential current-agent workflow; no Cockpit subagent runtime",
      ];
      ctx.ui.notify(checks.join("\\n"), checks.some((line) => line.startsWith("FAIL")) ? "error" : "info");
    },
  });
}
`;
}

function renderAgent(role) {
  const restrictions = role.readOnly ? "\ndisallowedTools: Write, Edit" : "";
  return `---
name: ${role.name}
description: ${role.description}
model: inherit
skills:
  - cockpit:${role.skill}${restrictions}
---

Load and follow the preloaded Cockpit skill. Stay within its handoff boundary and return only its requested output.
`;
}

function renderHook() {
  return `// Generated by scripts/generate-adapters.mjs. Do not edit directly.
const output = ${JSON.stringify({
    hookSpecificOutput: { hookEventName: "SessionStart", additionalContext: bootstrap("Claude") },
  })};
process.stdout.write(JSON.stringify(output) + "\\n");
`;
}

const outputs = new Map([
  [".opencode/plugins/cockpit.js", renderOpenCode()],
  ["extensions/cockpit.js", renderPi()],
  [".claude-plugin/plugin.json", JSON.stringify({ name: "cockpit", version: "0.1.0", description: "Skills-first software development workflows", author: { name: "Cockpit contributors" }, license: "MIT" }, null, 2) + "\n"],
  ["hooks/hooks.json", JSON.stringify({ hooks: { SessionStart: [{ hooks: [{ type: "command", command: "node", args: ["${CLAUDE_PLUGIN_ROOT}/hooks/session-start.mjs"] }] }] } }, null, 2) + "\n"],
  ["hooks/session-start.mjs", renderHook()],
  ["commands/cockpit-setup.md", `---\ndescription: Explain and verify Cockpit's Claude Code setup\n---\n\nVerify that the Cockpit plugin skills and five scoped agents are loaded. Explain that Claude agents inherit the current model, no model configuration is written, and the SessionStart hook loads using-cockpit. Do not change settings.\n`],
  ["commands/cockpit-doctor.md", `---\ndescription: Diagnose the Cockpit Claude Code plugin read-only\n---\n\nDiagnose this Cockpit plugin without changing files or settings. Verify the plugin, 11 scoped skills, five scoped agents, SessionStart bootstrap, and four read-only agent restrictions. Report PASS, WARN, or FAIL with exact remediation.\n`],
  ...roles.map((role) => [`agents/${role.name}.md`, renderAgent(role)]),
]);

let stale = false;
for (const [relative, content] of outputs) {
  const target = path.join(root, relative);
  const current = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : null;
  if (current === content) continue;
  if (check) {
    console.error(`Stale generated artifact: ${relative}`);
    stale = true;
    continue;
  }
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content);
  console.log(`Generated ${relative}`);
}

if (stale) process.exitCode = 1;
